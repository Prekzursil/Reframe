// NEVER emoji as icons (they render inconsistently across platforms and read as
// content, not chrome). These are decorative: the host marks the wrapping
// <span aria-hidden> and the visible tab label carries the accessible name.
//
// Shared geometry mirrors Lucide's defaults: 24×24 viewBox, currentColor stroke,
// width 2, round caps/joins — so `color` cascades from the tab's text color and
// the active accent automatically tints the glyph.
